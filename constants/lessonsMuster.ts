/**
 * Returns a robust curriculum/syllabus list for a given course name.
 * If the course is not found in the static muster database,
 * it dynamically generates 3 generic prep modules to ensure the UI never breaks.
 */
export function getSyllabusForCourse(courseName: string): string[] {
  // Replace characters & normalise spaces
  let normalizedKey = courseName.toUpperCase()
    .replace(/\s*-\s*/g, ' ') // normalize hyphens with space
    .replace(/\s+/g, ' ')
    .trim();

  // Normalize Turkish character variants to match keys precisely
  // E.g. "COGRAFYA" -> "COĞRAFYA", "BIYOLOJI" -> "BİYOLOJİ", etc.
  normalizedKey = normalizeTurkishSpelling(normalizedKey);

  // Check aliases
  if (normalizedKey.startsWith("YABANCI DİL")) {
    normalizedKey = normalizedKey.replace("YABANCI DİL", "İNGİLİZCE");
  }

  if (lessonsMuster[normalizedKey]) {
    return lessonsMuster[normalizedKey];
  }
  
  // Dynamic fallback modules
  return [
    "1. Ünite: Ders Konuları ve Temel Kavramlar",
    "2. Ünite: Sınava Hazırlık Soru Çözümleri",
    "3. Ünite: Çıkmış Sorular Analizi ve Değerlendirme"
  ];
}

/**
 * Normalizes spelling for common subjects to their standard key counterparts.
 */
function normalizeTurkishSpelling(text: string): string {
  let res = text;
  
  // Custom maps for high-frequency search keywords
  const subjectMaps: Record<string, string> = {
    "COGRAFYA": "COĞRAFYA",
    "FIZIK": "FİZİK",
    "BIYOLOJI": "BİYOLOJİ",
    "KIMYA": "KİMYA",
    "MATEMATIK": "MATEMATİK",
    "EDEBIYAT": "EDEBIYAT",
    "TURK DILI": "TÜRK DİLİ",
    "YABANCI DIL": "YABANCI DİL",
    "INGILIZCE": "İNGİLİZCE",
    "DIN KULTURU": "DİN KÜLTÜRÜ",
    "DIN KULTURU VE AHLAK BILGISI": "DİN KÜLTÜRÜ VE AHLAK BİLGİSİ"
  };

  // Check if any mapping keys are contained inside the text
  Object.keys(subjectMaps).forEach(k => {
    const regex = new RegExp(`\\b${k}\\b`, "g");
    res = res.replace(regex, subjectMaps[k]);
  });

  // Ensure overall vowel and glyph fixes for common AÖL titles
  res = res
    .replace(/TÜRK DİLİ VE EDEBİYATİ/g, "TÜRK DİLİ VE EDEBİYATI")
    .replace(/DİN KÜLTÜRÜ VE AHLAK BİLGİSİ/g, "DİN KÜLTÜRÜ VE AHLAK BİLGİSİ")
    .replace(/DİN KÜLTÜRÜ VE AHLAK BİLGİSİ/g, "DİN KÜLTÜRÜ VE AHLAK BİLGİSİ");

  return res;
}
